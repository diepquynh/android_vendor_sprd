/*****************************************************************************/
//! \brief  Description:  This function is to caculate eq a/b paras.
//!    it is an enhancement or extention function for Rec_Filter_CalcEqCoeffs
//! \author Author:  cherry.liu 
/*****************************************************************************/
BOOLEAN Rec_Filter_CalcEqCoeffs_Ex(
	int16_t  fo,                    //central freq in Hz  
	int16_t  df,                    //band width in Hz  
	int16_t  boostdB,               //range -720:1:180;  (-72dB ~ 18dB ,step:0.1dB)
    int16_t  basegaindB,            //range -840:1:60;  (-84dB ~ 6dB ,step:0.1dB)
	int32_t  Fs,                    //samplerate in Hz
	REC_IIR_FILTER_PARA_T* filterPara,  //out ptr,filter paras a,b
	int16_t*   sGain//OUT  scaled by 32768
)
{
    int16_t G0_Index=0,G1_Index=0;
    int16_t G0_fix=0,G1_fix=0,B_G_fix=0;
    int32_t EQ_B_GAIN=0;
    int32_t m=0,n_bit=0;
    
    int32_t B0=0,B1=0,B2=0,S=0;
    int32_t A0=0,A1=0,A2=0; 

    int32_t b_dividor=0,a_dividor=0;
    int64 b0_d=0,b1_d=0,a0_d=0,a1_d=0;

    //%all pass filter
    *sGain          = 32767;
    
    filterPara->B0  = 16384;
    filterPara->B1  = 0;
    filterPara->B2  = 0;
    
    filterPara->A0  = 16384;
    filterPara->A1  = 0;
    filterPara->A2  = 0;

    //check the validity of the input paras
	if(Fs<=0)
		return FALSE;

    if((basegaindB<-900)||(basegaindB>180))   
        return TRUE;
    
    //%add base gain;
    if(basegaindB<=0)
    {
        G1_Index  =  basegaindB + 900;
        *sGain    =  BaseGTable[G1_Index];//%scaled by 32768  base gain
    }
    else
    {
        *sGain    = 32767; //%scaled by 32768  %base gain
        
        G1_Index  =  basegaindB + 720;
        B_G_fix = GTable[G1_Index];

        if(B_G_fix>=8192)
        {
            filterPara->B0  = 32767;
        }
        else
        {
            filterPara->B0 = (filterPara->B0*B_G_fix)>>12;
            filterPara->B1 = (filterPara->B1*B_G_fix)>>12;
            filterPara->B2 = (filterPara->B2*B_G_fix)>>12;       
        }
    }


    if((boostdB<-720)||(boostdB>180))   // in para overflow
        return TRUE;

    if(0==boostdB)            //all pass case
        return TRUE;
    
    //%caculation begins 
    G0_Index  =  boostdB + 720;   
    G0_fix    =  GTable[G0_Index];//%scaled by 4096

    if(fo <= 0)
    {
        int16_t Fp=0;
        int16_t G0_ExTwo_fix=0,G0_ExFour_fix=0,Qeqfp=0;
        int32_t WP=0;
        int64 WP_d=0;
        int32_t Qk2dwp = 0;

        Fp    =   df;//% 3000;         % Pole Frequency
        G0_ExTwo_fix  =  GextractByTwoTable[G0_Index];//%scaled by 4096
        G0_ExFour_fix =  GextractByFourTable[G0_Index];//%scaled by 4096        
        
        Qeqfp  = (Fp<<13)/Fs;  //%redefinition Qeqfp:[0,4096]
        if((Qeqfp/2)>=2048) // in para overflow
		    return TRUE;
        WP_d   = (int64)(1<<26)*cos_Table[2048-Qeqfp/2];
        WP     = (int32_t)(WP_d/(G0_ExFour_fix*cos_Table[Qeqfp/2]));//%16384 归一化  int32_t
        if(WP==0)
        {
            WP=1;
        }
        
        //get dividor and dividends
        Qk2dwp = ((1<<28)/WP);
        b_dividor = Qk2dwp + (((int32_t)G0_ExTwo_fix*23170)>>12) + (int32_t)(((int64)WP*G0_fix)>>12); //% int32_t
        a_dividor = Qk2dwp + 23170 + WP ; //% int32_t

        b0_d = (((int64)WP*G0_fix)<<3) -((int64)Qk2dwp<<15);  //% int64
        b1_d = (((int64)Qk2dwp - (((int32_t)G0_ExTwo_fix*23170)>>12) + (((int64)WP*G0_fix)>>12))<<14) ;  //% int64
        a0_d = (((int64)WP-Qk2dwp)<<15)  ;          //% int64
        a1_d = (((int64)Qk2dwp - 23170 + WP)<<14) ; //% int64
        
        EQ_B_GAIN = (int32_t)(((int64)b_dividor<<12)/a_dividor);//%scaled by 4096  
    }
    else if (fo>=22050)
    {
        int16_t Fp=0;
        int16_t G0_ExTwo_fix=0,G0_ExFour_fix=0,Qeqfp=0;
        int32_t WP=0;
        int64 WP_d=0;
        int32_t Qk2dwp = 0;

        Fp    =   df;//% 3000;         % Pole Frequency
        G0_ExTwo_fix  =  GextractByTwoTable[G0_Index];//%scaled by 4096
        G0_ExFour_fix =  GextractByFourTable[G0_Index];//%scaled by 4096        

        Qeqfp  = (Fp<<13)/Fs; // %redefinition Qeqfp:[0,4096]
        if((Qeqfp/2)>=2048) // in para overflow
		    return TRUE;
        WP_d   = ((int64)(G0_ExFour_fix*cos_Table[2048-Qeqfp/2])<<14);
        WP     = (int32_t)(WP_d/(cos_Table[Qeqfp/2]<<12));// %16384 归一化  int32_t
        if(WP==0)
        {
            WP=1;
        }

        //get dividor and dividends
        Qk2dwp = ((1<<28)/WP);
        b_dividor = (int32_t)((((int64)Qk2dwp*G0_fix)>>12) + (((int32_t)G0_ExTwo_fix*23170)>>12) + WP );//% int32_t
        a_dividor = Qk2dwp + 23170 + WP;//% int32_t
        
        b0_d = ((int64)(WP-(((int64)Qk2dwp*G0_fix)>>12))<<15);   //% int64
        b1_d = ((((int64)Qk2dwp*G0_fix)>>12) - (((int32_t)G0_ExTwo_fix*23170)>>12) + WP)<<14;   //% int64
        a0_d = (((int64)WP-Qk2dwp)<<15);   //% int64
        a1_d = (((int64)Qk2dwp - 23170 + WP)<<14);   //% int64

        EQ_B_GAIN = (int32_t)(((int64)b_dividor<<12)/a_dividor);//%scaled by 4096  
    }
    else
    {
        int16_t QK=0,COSW0=0;
        int16_t Qeqbw=0,Qeqfo=0;
        int32_t GAMA=0;
        int32_t b_gain_dividor = 0;

        QK        =  GextractByTwoTable[G0_Index];//%scaled by 4096
   
        Qeqbw  = (df<<13)/Fs;  //%redefinition Qeqfp:[0,4096]
        if((Qeqbw/2)>=2048)
		    return TRUE;
        GAMA   = 16384*cos_Table[2048-Qeqbw/2]/cos_Table[Qeqbw/2]; //%16384 归一化  int32_t
  
        Qeqfo  = (fo<<13)/Fs;  //%redefinition
        if((Qeqfo/2)>=2048)
		    return TRUE;
        if(Qeqfo<=2048)
            COSW0 = cos_Table[Qeqfo];//%scaled by 16384
        else
            COSW0 = -cos_Table[4096-Qeqfo];//%scaled by 16384
 
        
        b_gain_dividor = (int32_t)(16384 + ((int64)GAMA<<12)/QK);//%int32_t
        b_dividor = (int32_t)(4096 + (((int64)GAMA*QK)>>14)); //%int32_t
        a_dividor = (QK<<2) + GAMA ;//%int32_t
        
        b0_d =  -(int64)COSW0<<13;        //%int64
        b1_d =  (int64)((1<<26) - (int64)GAMA*QK); //%int64
        a0_d =  -((int64)COSW0*QK)<<3;       //%int32_t ,for unification set it as int64
        a1_d =  (4*QK - (int64)GAMA)<<14; //%int64

        EQ_B_GAIN = (int32_t)(((int64)b_dividor<<14)/b_gain_dividor);//%scaled by 4096 
    }
    
    B0 = EQ_B_GAIN<<2;
    B1 = (int32_t)(((b0_d/b_dividor)*EQ_B_GAIN)>>12);
    B2 = (int32_t)(((b1_d/b_dividor)*EQ_B_GAIN)>>12);

    A0 = 16384;
    A1 = (int32_t)(a0_d/a_dividor);
    A2 = (int32_t)(a1_d/a_dividor);

    if(basegaindB<=0)
    {
        G1_Index  =  basegaindB + 900;
        G1_fix    =  BaseGTable[G1_Index];//%scaled by 32768  base gain
    }
    else
    {
        G1_fix    = 32767; //%scaled by 32768  %base gain
        
        G1_Index  =  basegaindB + 720;
        B0 = (B0*GTable[G1_Index])>>12;
        B1 = (B1*GTable[G1_Index])>>12;
        B2 = (B2*GTable[G1_Index])>>12;        
    }

    //%prevent B overflow
    m = max(abso(B0),abso(B1));
    m = max(m,abso(B2));
    n_bit = 0;

    while (m>32767)
    {
       m = m/2;
       n_bit= n_bit+1;
    }

    //calc S and adjust B
    if(0 == n_bit)
    {
        S = G1_fix;
    }
    else
    {
        B0 = B0>>n_bit;
        B1 = B1>>n_bit;
        B2 = B2>>n_bit;
        
        S  = G1_fix<<n_bit; 
        if(S>32767)
        {
            S=32767;
        }
    }

    //output paras
    *sGain          = S;
    filterPara->B0  = B0;
    filterPara->B1  = B1;
    filterPara->B2  = B2;
    filterPara->A0  = A0;
    filterPara->A1  = A1;
    filterPara->A2  = A2;

    return TRUE;
        
}

/*****************************************************************************/
//! \brief  Description:  This function is to caculate 200*log10(x)
//! \range of x [1,32767]
//! \author Author:  cherry.liu 
/*****************************************************************************/
int16_t F200log10(int32_t input)
{
    int16_t y  = 0;
    int16_t ii = 0;
    int32_t tp = 0; 
    int32_t t = 0,p = 0; 

    //check input para
    if(input>=32767)    
        input=32767;

    if(input<=0)
    {
        y = 0;
    }
    else
    {
        tp = input;
        //%normalization...............................
        ii = 15;
        while(ii>0)
        {
            //%less than 16384
            if(tp>16384)
            {
                break;
            }
            else
            {
                tp = tp*2;
            }
            
            ii = ii-1;
        }
        
        //%..........................................
        //%t    = (tp-32767)/32768;
        //%y    = fix(60.206*ii + 86.8458*(t-t*t/2+t*t*t/3-t*t*t*t/4));
        t     = tp-32767;

        p     = 10923 - (t>>2);
        p     = 16384 - (t*p>>15);
        p     = 32768 - (t*p>>15);
        p     =         (t*p>>15);
        y     = (int16_t)(( 7706*ii  + (11116*p>>15))>>7);
        
    }

    return y;
    
}

/*****************************************************************************/
//! \brief  Description:  This function is to caculate 32768*10^(-x/200)
//! \range of x [0,1023]
//! \author Author:  cherry.liu 
/*****************************************************************************/
int16_t F32768power10(int32_t input)
{
    int32_t cdbi =input;
    int16_t I=0,f=0,y=0;
    
    //% cdbi = cdbi &0x03ff
    if(cdbi<=0)
        cdbi=0;
    
    if(cdbi>=1023)
        cdbi = 1023;
    
        
    I = (cdbi>>4);      //% =cdbi>>4  (cdbi/16)>=0,then fix() = floor()
    f = cdbi-(I<<4);    //% = cdbi &0x000F

    y   = (cdB016amp[I]*cdB001amp[f]+8192)>>14;

    return y;

}


